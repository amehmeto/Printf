#include "libft.h"

void	ft_putnbr(int n)
{
	long	m;
	int		i;
	char	rslt[10];

	i = 0;
	m = n;
	if (m < 0)
	{
		ft_putchar('-');
		m *= -1;
	}
	rslt[i++] = m % 10 + '0';
	while (m /= 10)
		rslt[i++] = m % 10 + '0';
	i--;
	while (i >= 0)
		ft_putchar(rslt[i--]);
}
