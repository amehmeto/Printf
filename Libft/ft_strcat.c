#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	int		i;
	int		j;

	i = 0;
	j = (int)ft_strlen(s1) - 1;
	while (s2[i++])
		s1[i + j] = s2[i - 1];
	s1[i + j] = '\0';
	return (s1);
}
